# Dictionaries are a collection of key value pairs 
# Dictionaries are indexed by keys and can contain any type such as a string or numeric data type

my_dic = {'apple': 4, 'cotton': 6, 'BMW': 1, 'gixxer': 2}
print(my_dic['apple'])

# Dictionaries are mutable meaning they can add new key-pairs or modify existing ones 

my_dic['bunnies'] = 5 # Add new key-pair to dictionary 
print(my_dic)

my_dic['cotton'] = 9 # Modify key-pair in dictionary
print(my_dic)