# Loops in Python
# Loops allow you to execute a block of code repeatedly based on a condition or over a sequence.
# Python provides two main types of loops: `for` loops and `while` loops.

# For Loops

fruits = ["apple", "banana", "cherry"]

for fruit in fruits:
    print(fruit)
    
    numbers = [1, 2, 3, 5, 7]
for number in numbers:
    print(number)
    
# While Loops

count = 1

while count <= 5:
    print(count)
    count += 1  # Increment the count by 1