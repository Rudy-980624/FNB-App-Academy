# Numeric Data

# Integer - whole numbers
# Float - decimal numbers
# Complex - numbers with real and imaginary parts
# Boolean - True or False values
# Example of Numeric Data Types in Python

num = 10  # Integer
print(3)  # Output: 3
print(type(num))  # Output: <class 'int'>   
print("Integer:", num) # Output: Integer: 10

num2 = 3.14  # Float
print(num2)  # Output: 3.14
print(type(num2))  # Output: <class 'float'>
print("Float:", num2)  # Output: Float: 3.14

# Simpler than C/C++/Java as no need to declare data types explicitly

