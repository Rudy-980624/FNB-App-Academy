# Numeric Data Types in Python

# Numeric data types in Python include integers, floats, and complex numbers. 
# Integers are whole numbers, floats are decimal numbers, and complex numbers have a real and imaginary part.
# Example of Numeric Data Types in Python
num = 10  # Integer
print("Integer:", num)  # Output: Integer: 10
print(type(num))  # Output: <class 'int'>
num2 = 3.14  # Float
print("Float:", num2)  # Output: Float: 3.14
print(type(num2))  # Output: <class 'float'>
# Complex numbers are represented as a + bj, where a is the real part and b is the imaginary part
num3 = 2 + 3j  # Complex number
print("Complex Number:", num3)  # Output: Complex Number: (2+3j)
print(type(num3))  # Output: <class 'complex'>
# Python also has a boolean type, which can be either True or False.
# Boolean values are often used in conditional statements and logical operations.
is_true = True  # Boolean
print("Boolean:", is_true)  # Output: Boolean: True
print(type(is_true))  # Output: <class 'bool'>

# Numeric data types are used to perform mathematical operations and store numerical values.
# Example of Mathematical Operations
sum_result = num + num2  # Adding an integer and a float
print("Sum of Integer and Float:", sum_result)  # Output: Sum of Integer and Float: 13.14
difference_result = num - num2  # Subtracting a float from an integer
print("Difference of Integer and Float:", difference_result)  # Output: Difference of Integer and Float: 6.86
product_result = num * num2  # Multiplying an integer and a float
print("Product of Integer and Float:", product_result)  # Output: Product of Integer and Float: 31.4
division_result = num / num2  # Dividing an integer by a float
print("Division of Integer by Float:", division_result)  # Output: Division of Integer by Float: 3.1830985915492954
modulus_result = num % 3  # Modulus operation
print("Modulus of Integer by 3:", modulus_result)  # Output: Modulus of Integer by 3: 1
exponentiation_result = num ** 2  # Exponentiation operation
print("Exponentiation of Integer:", exponentiation_result)  # Output: Exponentiation of
# Integer: 100
floor_division_result = num // 3  # Floor division operation
print("Floor Division of Integer by 3:", floor_division_result)  # Output:
# Floor Division of Integer by 3: 3

# Numeric data types in Python are dynamic, meaning you don't need to declare their type explicitly.
# You can assign any numeric value to a variable without specifying its type.
# This makes Python simpler than languages like C, C++, or Java, where you need to declare data types explicitly.

