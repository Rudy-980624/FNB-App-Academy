# Operators in Python are special symbols that perform operations on variables and values. Python divides the operators in the following groups:
# Arithmetic Operators, Comparison Operators, Assignment Operators, Logical Operators, Bitwise Operators, Membership Operators, Identity Operators.

# Arithmetic Operators
# These operators are used to perform mathematical operations on numeric values.

# Addition (+)
# This operator is used to add two values.
# Example: x + y

# Subtraction (-)
# This operator is used to subtract one value from another.
# Example: x - y

# Multiplication (*)
# This operator is used to multiply two values.
# Example: x * y

# Division (/)
# This operator is used to divide one value by another and returns a float.
# Example: x / y

# Modulus (%)
# This operator is used to find the remainder of the division of one value by another.
# Example: x % y

# Exponentiation (**)
# This operator is used to raise one value to the power of another.
# Example: x ** y

# Floor Division (//)
# This operator is used to divide one value by another and returns the largest integer less than or equal to the result.
# Example: x // y


# Comparison Operators
# These operators are used to compare two values and return a boolean result (True or False).

# # Equal to (==)
# # This operator checks if two values are equal.
# # Example: x == y

# # Not equal to (!=)
# # This operator checks if two values are not equal.
# # Example: x != y

# # Greater than (>)
# # This operator checks if one value is greater than another.
# # Example: x > y

# # Less than (<)
# # This operator checks if one value is less than another.
# # Example: x < y

# # Greater than or equal to (>=)
# # This operator checks if one value is greater than or equal to another.
# # Example: x >= y

# # Less than or equal to (<=)
# # This operator checks if one value is less than or equal to another.
# # Example: x <= y

# Assignment Operators
# These operators are used to assign values to variables.

# # Assignment (=)
# # This operator is used to assign a value to a variable.
# # Example: x = 10

# # Add and assign (+=)
# # This operator adds a value to a variable and assigns the result back to that variable.
# # Example: x += 5  # Equivalent to x = x + 5

# # Subtract and assign (-=)
# # This operator subtracts a value from a variable and assigns the result back to that variable.
# # Example: x -= 5  # Equivalent to x = x - 5

# # Multiply and assign (*=)
# # This operator multiplies a variable by a value and assigns the result back to that variable.
# # Example: x *= 5  # Equivalent to x = x * 5

# # Divide and assign (/=)
# # This operator divides a variable by a value and assigns the result back to that variable.
# # Example: x /= 5  # Equivalent to x = x / 5

# # Modulus and assign (%=)
# # This operator finds the modulus of a variable with a value and assigns the result back to that variable.
# # Example: x %= 5  # Equivalent to x = x % 5

# # Exponentiate and assign (**=)
# # This operator raises a variable to the power of a value and assigns the result back to that variable.
# # Example: x **= 5  # Equivalent to x = x ** 5

# Logical Operators
# These operators are used to combine conditional statements.

# # Logical AND (and)
# # This operator returns True if both operands are True.
# # Example: x > 5 and y < 10

# # Logical OR (or)
# # This operator returns True if at least one of the operands is True.
# # Example: x > 5 or y < 10

# # Logical NOT (not)
# # This operator reverses the logical state of its operand.
# # Example: not (x > 5)

# Bitwise Operators
# These operators are used to perform bit-level operations on integers.

# # Bitwise AND (&)
# # This operator performs a bitwise AND operation on two integers.
# # Example: x & y

# # Bitwise OR (|)
# # This operator performs a bitwise OR operation on two integers.
# # Example: x | y

# # Bitwise XOR (^)
# # This operator performs a bitwise XOR operation on two integers.
# # Example: x ^ y

# # Bitwise NOT (~)
# # This operator performs a bitwise NOT operation on an integer.
# # Example: ~x

# # Left shift (<<)
# # This operator shifts the bits of an integer to the left by a specified number of positions.
# # Example: x << 2  # Shifts bits of x to the left by 2 positions

# # Right shift (>>)
# # This operator shifts the bits of an integer to the right by a specified number of positions.
# # Example: x >> 2  # Shifts bits of x to the right by

# Membership Operators
# These operators are used to test membership in sequences like lists, tuples, and strings.

# # In (in)
# # This operator checks if a value is present in a sequence.
# # Example: x in my_list

# # Not in (not in)
# # This operator checks if a value is not present in a sequence.
# # Example: x not in my_list

# Identity Operators
# These operators are used to compare the memory locations of two objects.

# # Is (is)
# # This operator checks if two variables point to the same object in memory.
# # Example: x is y

# # Is not (is not)
# # This operator checks if two variables do not point to the same object in memory.
# # Example: x is not y

